package Mar_2026;

public class Lab_27Mar_2026_EvenOdd
{
    public static void main(String[] args)
    {
        int a= 50;
        for (int i=1;i<=a;i++)
        {
            if(i%2==0)
            {
                System.out.println(i+ " is even ");
            }
            else
            {
                System.out.println(i+ " is odd ");
            }
        }

    }
}
